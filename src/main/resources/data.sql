create table integrante (
                            id bigint generated by default as identity,
                            franquia varchar(255) not null,
                            funcao varchar(255) not null,
                            nome varchar(255) not null,
                            primary key (id)
);

create table time (
                      id bigint generated by default as identity,
                      data date,
                      primary key (id)
);

create table composicao_time (
                                 id bigint generated by default as identity,
                                 integrante_id bigint,
                                 time_id bigint,
                                 primary key (id)
);

